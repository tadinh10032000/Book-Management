    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int id = Integer.parseInt(request.getParameter("id"));
        AdminDAO dao = new AdminDAO();
        Book b = new Book();
        b = dao.getOne(id);
        request.setAttribute("one", b);
        request.getRequestDispatcher("Update.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            int id = Integer.parseInt(request.getParameter("uid"));
            String title = request.getParameter("utitle");
            String author = request.getParameter("uauthor");
            String brief = request.getParameter("ubrief");
            String publisher = request.getParameter("upublisher");
            String content = request.getParameter("ucontent");
            String category = request.getParameter("ucategory");
            Book b = new Book(id, title, author, brief, publisher, content, category);
            AdminDAO dao = new AdminDAO();
            boolean a = dao.update(b);
            if (a == true) {
                request.setAttribute("message", "Update Successfully!!");
            } else {
                request.setAttribute("message", "Update Failed!!");
            }
            request.getRequestDispatcher("Admin.jsp").forward(request, response);
        } catch (Exception e) {

        }
    }

